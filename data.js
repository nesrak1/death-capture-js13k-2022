var dataStr, dataPos, dataMask;
//0123456789:;<=>?@ABCDEFGHIJKLMNOPQRSTUVWXYZ[/]^_`abcdefghijklmno
function eggo(i) {
    i = i.charCodeAt(0);
    return (i == 0x2f) ? (0x5c - 0x30) : (i - 0x30); //stupid \
}
function decode(bitlen) {
    var value = 0;
    var valueMask = 1;
    for (var i = 0; i < bitlen; i++) {
        var dataValue = eggo(dataStr[dataPos]||"0");
        value |= (((dataValue & dataMask) != 0) ? -1 : 0) & valueMask;
        valueMask <<= 1;
        dataMask <<= 1;
        if (dataMask >= 64) {
            dataMask = 1;
            dataPos++;
        }
    }
    return value;
}
function load(str) {
    dataStr = str;
    dataPos = 0;
    dataMask = 1;
}

